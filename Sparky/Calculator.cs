namespace Sparky;

public class Calculator
{
    public int AddCalculater(int a, int b)
    {
        return a + b;
    }

    public bool OddNumberCalculate(int a)
    {
        return a % 2 != 0;
    }


    public double AddDoubleCalculator(double a, double b)
    {
        return a + b;
    }


}